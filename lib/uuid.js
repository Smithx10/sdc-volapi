var UUID_RE = /^[a-f0-9]{8}-[a-f0-9]{4}-[a-f0-9]{4}-[a-f0-9]{4}-[a-f0-9]{12}$/;

function validUuid(uuid) {
    return typeof (uuid) === 'string' && uuid !== '' && uuid.match(UUID_RE);
}

module.exports = {
    validUuid: validUuid
};